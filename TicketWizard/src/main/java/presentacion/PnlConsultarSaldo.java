    private void btnAgregarSaldoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarSaldoActionPerformed
        String strDoble = JOptionPane.showInputDialog(
                parent,
                "Ingrese la cantidad que desea a침adir",
                "A침adir Saldo",
                JOptionPane.QUESTION_MESSAGE);
        try{
            if(!Seguridad.validaDoble(strDoble)){
                mostrarErrorFormato();
                return;
            }
            double saldo = Double.parseDouble(strDoble);
            control.agregarSaldo(saldo, parent.getUsuario());
            cargarSaldo();
        } catch(NumberFormatException ex){
            mostrarErrorFormato();
        }
    }//GEN-LAST:event_btnAgregarSaldoActionPerformed
    
    private void mostrarErrorFormato(){
        JOptionPane.showMessageDialog(
                parent,
                "Asegurese de ingresar un numero entero o decimal, con solo 2 decimales",
                "Error",
                JOptionPane.ERROR_MESSAGE);
    }

